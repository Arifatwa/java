package belajar;
public class operator_aritmatika {
    public static void main(String[] args) {
        // belajar operator aritmatika
        int a = 10;
        int b = 5;
        int hasil;
        
        // penjumlahan
        hasil = a + b;
        System.out.println("a + b = " + hasil );
        
        // pengurangan
        hasil = a - b;
        System.out.println("a - b = " + hasil );
        
        // perkalian
        hasil = a * b;
        System.out.println("a x b = " + hasil);
        
        // pembagian
        hasil = a / b;
        System.out.println("a / b = " + hasil);
        
        // modulus (sisa bagi)
        hasil = a % b;
        System.out.println("a & b = " + hasil);

        
        
    }
}
